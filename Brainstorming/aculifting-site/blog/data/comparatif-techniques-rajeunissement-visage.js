    <p>Le <strong>massage facial classique</strong> regroupe un ensemble de techniques manuelles &mdash; effleurages, pressions, pétrissages, drainages lymphatiques &mdash; pratiquées par des esthéticiennes, facialistes ou thérapeutes. Moins codifié que le kobido, il peut intégrer différentes écoles et approches (massage californien du visage, drainage Vodder, massage Renata França...).</p>

