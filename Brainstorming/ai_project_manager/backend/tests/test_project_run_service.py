"""Unit tests for `ProjectRunService` with the agents + LLM mocked.

Strategy: instead of mocking `common_tools.llm.Llm.ainvoke` (which the
service only touches indirectly through `DevAgent`, `QaAgent` and
`OrchestratorAgent`), we inject **fake agent factories** into the
service. Each fake agent implements the same async surface as its real
counterpart but returns hard-coded payloads.

This gives us:

- deterministic DevAgent output (a single file per task in a tmp dir),
- deterministic QaAgent verdicts (approved / rejected on demand),
- deterministic OrchestratorAgent picks (all executable tasks in order),

without ever hitting the LLM provider or importing common-tools.

We also override `session_factory` so the background task, which
normally opens fresh `AsyncSessionLocal` sessions, uses the in-memory
SQLite factory spun up by the test.
"""

