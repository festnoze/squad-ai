@pytest.mark.asyncio
async def test_run_walks_linear_chain_to_success(run_env) -> None:
    """3 tasks, each depending on the previous → all end in ``done``."""
    session_factory, workspace = run_env
    project_id, task_ids = await _aseed_tasks(
        session_factory,
        count=3,
        deps=[(1, 0), (2, 1)],
    )

    service = _build_service(session_factory, workspace)

    async with session_factory() as session:
        run = await service.astart_project_run(
            session=session,
            project_id=project_id,
        )
        assert run.id is not None
        assert run.total_tasks == 3

    await _await_run_status(
        session_factory,
        run.id,
        ProjectRunStatus.SUCCEEDED,
    )

    async with session_factory() as session:
        item_repo = ItemRepository(session)
        items = await item_repo.aget_items_by_project(project_id)
    for task_id in task_ids:
        match = next(it for it in items if it.id == task_id)
        assert match.status == ItemStatus.DONE
        assert match.deliverable_paths == ["main.py"]


@pytest.mark.asyncio
async def test_run_refuses_second_concurrent_run(run_env) -> None:
    """Starting a second run while one is active raises `ProjectRunConflict`."""
    session_factory, workspace = run_env
    project_id, _ = await _aseed_tasks(session_factory, count=1)

    service = _build_service(session_factory, workspace)

    async with session_factory() as session:
        await service.astart_project_run(session=session, project_id=project_id)

    from app.application.project_run_service import ProjectRunConflict

    async with session_factory() as session:
        with pytest.raises(ProjectRunConflict):
            await service.astart_project_run(
                session=session,
                project_id=project_id,
            )


@pytest.mark.asyncio
async def test_run_retries_on_qa_reject_then_succeeds(run_env) -> None:
    """QA rejects once, dev retries, second QA approves → task done."""
    session_factory, workspace = run_env
    project_id, task_ids = await _aseed_tasks(session_factory, count=1)

    qa_fake = FakeQaAgentRejectThenApprove()
    service = _build_service(
        session_factory,
        workspace,
        qa_factory=lambda: qa_fake,
    )

    async with session_factory() as session:
        run = await service.astart_project_run(
            session=session,
            project_id=project_id,
        )
        assert run.id is not None

    await _await_run_status(
        session_factory,
        run.id,
        ProjectRunStatus.SUCCEEDED,
    )

    async with session_factory() as session:
        item_repo = ItemRepository(session)
        items = await item_repo.aget_items_by_project(project_id)
    task = next(it for it in items if it.id == task_ids[0])
    assert task.status == ItemStatus.DONE
    assert qa_fake.calls == 2
    assert task.deliverable_notes is not None
    assert "rejet" in task.deliverable_notes


@pytest.mark.asyncio
async def test_run_blocks_task_after_double_rejection(run_env) -> None:
    """QA rejects twice → task ends in ``blocked`` with a reason."""
    session_factory, workspace = run_env
    project_id, task_ids = await _aseed_tasks(session_factory, count=1)

    service = _build_service(
        session_factory,
        workspace,
        qa_factory=lambda: FakeQaAgentAlwaysReject(),
    )

    async with session_factory() as session:
        run = await service.astart_project_run(
            session=session,
            project_id=project_id,
        )
        assert run.id is not None

    await _await_run_status(
        session_factory,
        run.id,
        ProjectRunStatus.SUCCEEDED,
    )

    async with session_factory() as session:
        item_repo = ItemRepository(session)
        items = await item_repo.aget_items_by_project(project_id)
        run_repo = ProjectRunRepository(session)
