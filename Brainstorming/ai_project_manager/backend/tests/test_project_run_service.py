@pytest_asyncio.fixture
async def run_env(
    tmp_path: Path,
) -> AsyncGenerator[tuple[async_sessionmaker[AsyncSession], Path], None]:
    """Provide a private in-memory DB + a sandbox workspace for DevAgents."""
    # StaticPool is required so the multiple short-lived sessions the
    # service opens (via `AsyncSessionLocal`) all hit the **same**
    # in-memory SQLite database. Without it, each new connection gets
    # its own empty `:memory:` and writes from iteration N are invisible
    # to iteration N+1.
    engine = create_async_engine(
        "sqlite+aiosqlite:///:memory:",
        future=True,
        connect_args={"check_same_thread": False},
        poolclass=StaticPool,
    )
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    session_factory = async_sessionmaker(
        bind=engine,
        expire_on_commit=False,
        autoflush=False,
        autocommit=False,
        class_=AsyncSession,
    )

    workspace = tmp_path / "generated"
    workspace.mkdir()

    try:
        yield session_factory, workspace
    finally:
        await engine.dispose()


async def _aseed_tasks(
    session_factory: async_sessionmaker[AsyncSession],
    *,
    count: int,
    deps: list[tuple[int, int]] | None = None,
) -> tuple[UUID, list[UUID]]:
    """Seed ``count`` TODO tasks with optional deps by task index.

    Deps is a list of `(downstream_idx, upstream_idx)` tuples meaning
    ``downstream depends on upstream``.
    """
    deps = deps or []
    async with session_factory() as session:
        project_repo = ProjectRepository(session)
        project = await project_repo.acreate_project(Project(name="RunSvcProj"))
        assert project.id is not None

        item_repo = ItemRepository(session)
        task_ids: list[UUID] = []
        for i in range(count):
            task = await item_repo.acreate_item(
                Item(
                    project_id=project.id,
                    type=ItemType.TASK,
                    title=f"Task {i}",
                    description=f"Does thing #{i}",
                    status=ItemStatus.TODO,
                ),
            )
            assert task.id is not None
            task_ids.append(task.id)

        dep_repo = ItemDependencyRepository(session)
        for downstream_idx, upstream_idx in deps:
            await dep_repo.acreate_dependency(
                ItemDependency(
                    item_id=task_ids[downstream_idx],
                    depends_on_item_id=task_ids[upstream_idx],
                ),
            )

        await session.commit()
        return project.id, task_ids


async def _await_run_status(
    session_factory: async_sessionmaker[AsyncSession],
    run_id: UUID,
    target_status: ProjectRunStatus,
    *,
    timeout_seconds: float = 5.0,
) -> None:
    """Poll the run status until it matches ``target_status`` or timeout."""
    deadline = asyncio.get_event_loop().time() + timeout_seconds
    while asyncio.get_event_loop().time() < deadline:
        async with session_factory() as session:
            repo = ProjectRunRepository(session)
            run = await repo.aget_run_by_id(run_id)
        if run is not None and run.status == target_status:
            return
        await asyncio.sleep(0.02)
    pytest.fail(
        f"Run {run_id} did not reach status {target_status} within "
        f"{timeout_seconds}s",
    )


def _build_service(
    session_factory: async_sessionmaker[AsyncSession],
    workspace: Path,
    *,
    qa_factory=lambda: FakeQaAgentApprove(),
) -> ProjectRunService:
    fake_dev = FakeDevAgent(workspace_root=workspace)
    return ProjectRunService(
        dev_agent_factory=lambda: fake_dev,  # type: ignore[arg-type,return-value]
        qa_agent_factory=qa_factory,  # type: ignore[arg-type]
        orchestrator_factory=lambda: FakeOrchestratorAgent(),  # type: ignore[arg-type,return-value]
        session_factory=session_factory,
        max_parallel=2,
        max_iterations=2,
    )


