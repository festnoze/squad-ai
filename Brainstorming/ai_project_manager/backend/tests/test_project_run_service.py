class FakeDevAgent:
    """DevAgent replacement that writes a single file to a tmp dir."""

    def __init__(self, workspace_root: Path) -> None:
        self.workspace_root = workspace_root
        self.calls: list[UUID] = []

    async def aproduce_deliverable(
        self,
        task: Item,
        qa_feedback: str | None = None,
    ) -> DevAgentResult:
        assert task.id is not None
        self.calls.append(task.id)
        task_dir = self.workspace_root / str(task.project_id) / str(task.id)
        task_dir.mkdir(parents=True, exist_ok=True)
        file_path = task_dir / "main.py"
        file_path.write_text(
            f"# Generated by FakeDevAgent for {task.title}\nprint('hello')\n",
            encoding="utf-8",
        )
        return DevAgentResult(
            summary=f"Fake code for {task.title}",
            files=[
                DevFileOutput(
                    absolute_path=file_path,
                    relative_path="main.py",
                    language="python",
                ),
            ],
            raw_response={"summary": "fake"},
        )


class FakeQaAgentApprove:
    """QaAgent replacement that always approves."""

    async def areview_deliverable(
        self,
        task: Item,
        dev_result: DevAgentResult,
    ) -> QaAgentResult:
        return QaAgentResult(
            verdict=QaVerdict.APPROVED,
            feedback=f"OK for {task.title}",
            raw_response={"verdict": "approved"},
        )


class FakeQaAgentRejectThenApprove:
    """QaAgent that rejects the first call and approves the rest.

    Lets us exercise the dev/QA loop on a single task.
    """

    def __init__(self) -> None:
        self.calls = 0

    async def areview_deliverable(
        self,
        task: Item,
        dev_result: DevAgentResult,
    ) -> QaAgentResult:
        self.calls += 1
        if self.calls == 1:
            return QaAgentResult(
                verdict=QaVerdict.REJECTED,
                feedback="Missing tests.",
                raw_response={"verdict": "rejected"},
            )
        return QaAgentResult(
            verdict=QaVerdict.APPROVED,
            feedback="Fixed.",
            raw_response={"verdict": "approved"},
        )


class FakeQaAgentAlwaysReject:
    async def areview_deliverable(
        self,
        task: Item,
        dev_result: DevAgentResult,
    ) -> QaAgentResult:
        return QaAgentResult(
            verdict=QaVerdict.REJECTED,
            feedback="Still bad.",
            raw_response={"verdict": "rejected"},
        )


class FakeOrchestratorAgent:
    """OrchestratorAgent replacement: picks all executable tasks in order."""

    async def apick_next_batch(
        self,
        executable_tasks: list[Item],
        max_parallel: int,
    ) -> OrchestratorDecision:
        picks = [
            t.id
            for t in executable_tasks[:max_parallel]
            if t.id is not None
        ]
        return OrchestratorDecision(
            selected_task_ids=picks,
            rationale="Test fake: take everything.",
        )


