"""P3 — reliable worktree node_modules: usability probe, junction that reports
whether it resolved, and the real-install fallback when the junction fails (the
recurring Windows `mklink /J` trap)."""

from autospec.config import settings
from autospec.models import Epic, ProjectState, Stream, StreamKind
from autospec.orchestrator import workspace
from autospec.orchestrator.pipeline import Pipeline
from autospec.agents.scripted import ScriptedRunner


def test_node_modules_usable_detects_empty_vs_installed(tmp_path):
    assert not Pipeline._node_modules_usable(tmp_path)          # nothing
    (tmp_path / "node_modules").mkdir()
    assert not Pipeline._node_modules_usable(tmp_path)          # present but empty/broken
    (tmp_path / "node_modules" / ".bin").mkdir()
    assert Pipeline._node_modules_usable(tmp_path)              # has .bin → usable


def test_link_node_modules_reports_resolution(tmp_path):
    target = tmp_path / "main" / "node_modules"
    (target / ".bin").mkdir(parents=True)
    link = tmp_path / "wt" / "node_modules"
    ok = Pipeline._link_node_modules(link, target)
    assert ok is True
    assert Pipeline._node_modules_usable(tmp_path / "wt")


def _fe_state(pid="fe-nm"):
    st = ProjectState(id=pid, name="feapp", goal="g")
    st.epics.append(Epic(id="EPIC-1", title="E"))
    st.streams = [
        Stream(id="backend", kind=StreamKind.BACKEND, language="python", primary=True),
        Stream(id="frontend", kind=StreamKind.FRONTEND, language="react", file_root="frontend"),
    ]
    return st


async def test_ensure_falls_back_to_real_install_when_junction_fails(tmp_path, monkeypatch):
    monkeypatch.setattr(settings, "fake_agents", False)
    main_root = tmp_path / "frontend"
    (main_root).mkdir()
    (main_root / "package.json").write_text("{}", encoding="utf-8")
    (main_root / "node_modules" / ".bin").mkdir(parents=True)   # main install usable
    worktree_root = tmp_path / "wt" / "frontend"
    worktree_root.mkdir(parents=True)

    stream = Stream(id="frontend", kind=StreamKind.FRONTEND, language="react", file_root="frontend")
    monkeypatch.setattr(workspace, "frontend_streams", lambda state: [stream])
    monkeypatch.setattr(workspace, "stream_root", lambda state, s: main_root)

    pipeline = Pipeline(_fe_state(), ScriptedRunner())
    # Simulate the junction silently failing (Windows mklink /J trap).
    monkeypatch.setattr(pipeline, "_link_node_modules", lambda link, target: False)
    calls: list = []

    async def _fake_install(root, *, use_ci):
        calls.append((root, use_ci))
        (root / "node_modules" / ".bin").mkdir(parents=True, exist_ok=True)
        return True

    monkeypatch.setattr(pipeline, "_anpm_install_in", _fake_install)

    await pipeline._aensure_frontend_node_modules(worktree_root)

    # The main install was usable (not reinstalled); only the worktree fell back.
    assert calls == [(worktree_root, True)]


async def test_ensure_uses_junction_when_it_resolves(tmp_path, monkeypatch):
    monkeypatch.setattr(settings, "fake_agents", False)
    main_root = tmp_path / "frontend"
    main_root.mkdir()
    (main_root / "package.json").write_text("{}", encoding="utf-8")
    (main_root / "node_modules" / ".bin").mkdir(parents=True)
    worktree_root = tmp_path / "wt" / "frontend"
    worktree_root.mkdir(parents=True)

    stream = Stream(id="frontend", kind=StreamKind.FRONTEND, language="react", file_root="frontend")
    monkeypatch.setattr(workspace, "frontend_streams", lambda state: [stream])
    monkeypatch.setattr(workspace, "stream_root", lambda state, s: main_root)

    pipeline = Pipeline(_fe_state("fe-nm2"), ScriptedRunner())
    monkeypatch.setattr(pipeline, "_link_node_modules", lambda link, target: True)  # junction OK
    called = []
    monkeypatch.setattr(pipeline, "_anpm_install_in", lambda root, *, use_ci: called.append(root))

    await pipeline._aensure_frontend_node_modules(worktree_root)

    assert called == []  # junction resolved → no per-worktree install
